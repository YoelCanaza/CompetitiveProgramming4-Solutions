#include <bits/stdc++.h>
using namespace std;

typedef long long Long;
const char ENDL = '\n';

void solve(void) {
    int n, m; cin >> n >> m;
    int s = (n/3) * (m/3);
    cout << s << ENDL;
}

int main(void) {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t; cin >> t;
    while (t--) solve();

    return 0;
}